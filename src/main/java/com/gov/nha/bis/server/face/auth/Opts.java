package com.gov.nha.bis.server.face.auth;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlRootElement;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlRootElement(name="Opts")
public class Opts {

	@XmlAttribute
	protected String fCount;

	@XmlAttribute
	protected String fType;

	@XmlAttribute
	protected String iCount;

	@XmlAttribute
	protected String iType;

	@XmlAttribute
	protected String pCount;

	@XmlAttribute
	protected String pType;

	@XmlAttribute
	protected String format;

	@XmlAttribute
	protected String pidVer;

	@XmlAttribute
	protected String timeout;

	@XmlAttribute
	protected String otp;

	@XmlAttribute
	protected String wadh;

	@XmlAttribute
	protected String posh;

	
	public String getfCount() {
		return fCount;
	}

	public void setfCount(String fCount) {
		this.fCount = fCount;
	}

	public String getfType() {
		return fType;
	}

	public void setfType(String fType) {
		this.fType = fType;
	}

	public String getiCount() {
		return iCount;
	}

	public void setiCount(String iCount) {
		this.iCount = iCount;
	}

	public String getiType() {
		return iType;
	}

	public void setiType(String iType) {
		this.iType = iType;
	}

	public String getpCount() {
		return pCount;
	}

	public void setpCount(String pCount) {
		this.pCount = pCount;
	}

	public String getpType() {
		return pType;
	}

	public void setpType(String pType) {
		this.pType = pType;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public String getPidVer() {
		return pidVer;
	}

	public void setPidVer(String pidVer) {
		this.pidVer = pidVer;
	}

	public String getTimeout() {
		return timeout;
	}

	public void setTimeout(String timeout) {
		this.timeout = timeout;
	}

	public String getOtp() {
		return otp;
	}

	public void setOtp(String otp) {
		this.otp = otp;
	}

	public String getWadh() {
		return wadh;
	}

	public void setWadh(String wadh) {
		this.wadh = wadh;
	}

	public String getPosh() {
		return posh;
	}

	public void setPosh(String posh) {
		this.posh = posh;
	}

}
