package com.gov.nha.bis.server.preauth_requestResponse;

public class CaseAttachDetails {
	 private String image;
	 private String fileName;
	 private String attachName;
	 private String attachShortName;
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getAttachName() {
		return attachName;
	}
	public void setAttachName(String attachName) {
		this.attachName = attachName;
	}
	public String getAttachShortName() {
		return attachShortName;
	}
	public void setAttachShortName(String attachShortName) {
		this.attachShortName = attachShortName;
	}
	@Override
	public String toString() {
		return "CaseAttachDetails [image=" + image + ", fileName=" + fileName + ", attachName=" + attachName
				+ ", attachShortName=" + attachShortName + "]";
	}
	 
	 
}
