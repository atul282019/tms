package com.gov.nha.bis.server.requestResponse;

import java.io.Serializable;
import java.util.Arrays;

public class ShowStratificationDataReq implements Serializable{
	

	private static final long serialVersionUID = -5145118965670277166L;
	
	private String txnid;
	private String[] stratCode;
	private String speciality;
	private String packageid;
	private String procedureid;
	
	@Override
	public String toString() {
		return "ShowStratificationDataReq [txnid=" + txnid + ", stratCode=" + Arrays.toString(stratCode)
				+ ", speciality=" + speciality + ", packageid=" + packageid + ", procedureid=" + procedureid + "]";
	}
	public String getSpeciality() {
		return speciality;
	}
	public void setSpeciality(String speciality) {
		this.speciality = speciality;
	}
	public String getPackageid() {
		return packageid;
	}
	public void setPackageid(String packageid) {
		this.packageid = packageid;
	}
	public String getProcedureid() {
		return procedureid;
	}
	public void setProcedureid(String procedureid) {
		this.procedureid = procedureid;
	}
	public String getTxnid() {
		return txnid;
	}
	public void setTxnid(String txnid) {
		this.txnid = txnid;
	}
	public String[] getStratCode() {
		return stratCode;
	}
	public void setStratCode(String[] stratCode) {
		this.stratCode = stratCode;
	}
	
	
}
