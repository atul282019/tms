package com.gov.nha.bis.server.preauth_requestResponse;

public class ImplantDetails {
	private String txnid;
	private String speciality;
	private String packageid;
	private String procedureid;
	private String implantCode;
	private String implantName;
	private String implantCost;
	private String implantCount;
	private String maximumMultiplier;
	private String totalImplantCost;
	public String getTxnid() {
		return txnid;
	}
	public void setTxnid(String txnid) {
		this.txnid = txnid;
	}
	public String getImplantCode() {
		return implantCode;
	}
	public void setImplantCode(String implantCode) {
		this.implantCode = implantCode;
	}
	public String getImplantName() {
		return implantName;
	}
	public void setImplantName(String implantName) {
		this.implantName = implantName;
	}
	public String getImplantCost() {
		return implantCost;
	}
	public void setImplantCost(String implantCost) {
		this.implantCost = implantCost;
	}
	public String getImplantCount() {
		return implantCount;
	}
	public void setImplantCount(String implantCount) {
		this.implantCount = implantCount;
	}
	public String getMaximumMultiplier() {
		return maximumMultiplier;
	}
	public void setMaximumMultiplier(String maximumMultiplier) {
		this.maximumMultiplier = maximumMultiplier;
	}
	public String getTotalImplantCost() {
		return totalImplantCost;
	}
	public void setTotalImplantCost(String totalImplantCost) {
		this.totalImplantCost = totalImplantCost;
	}
	@Override
	public String toString() {
		return "ImplantDetails [txnid=" + txnid + ", speciality=" + speciality + ", packageid=" + packageid
				+ ", procedureid=" + procedureid + ", implantCode=" + implantCode + ", implantName=" + implantName
				+ ", implantCost=" + implantCost + ", implantCount=" + implantCount + ", maximumMultiplier="
				+ maximumMultiplier + ", totalImplantCost=" + totalImplantCost + "]";
	}
	public String getSpeciality() {
		return speciality;
	}
	public void setSpeciality(String speciality) {
		this.speciality = speciality;
	}
	public String getPackageid() {
		return packageid;
	}
	public void setPackageid(String packageid) {
		this.packageid = packageid;
	}
	public String getProcedureid() {
		return procedureid;
	}
	public void setProcedureid(String procedureid) {
		this.procedureid = procedureid;
	}	
}
