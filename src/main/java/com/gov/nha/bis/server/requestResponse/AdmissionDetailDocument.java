package com.gov.nha.bis.server.requestResponse;

import java.io.Serializable;

import org.springframework.web.multipart.MultipartFile;

public class AdmissionDetailDocument implements Serializable{
	

	/**
	 * 
	 */
	private static final long serialVersionUID = -5145118965670277166L;

	public MultipartFile adddocFile;
	
	private String documentid;
	private String documenttxt;
	private String documentname;
	private String txnId;
	private String tempId;
	
	public MultipartFile getAdddocFile() {
		return adddocFile;
	}

	public void setAdddocFile(MultipartFile adddocFile) {
		this.adddocFile = adddocFile;
	}

	public String getDocumentid() {
		return documentid;
	}

	public void setDocumentid(String documentid) {
		this.documentid = documentid;
	}

	public String getDocumenttxt() {
		return documenttxt;
	}

	public void setDocumenttxt(String documenttxt) {
		this.documenttxt = documenttxt;
	}

	public String getDocumentname() {
		return documentname;
	}

	public void setDocumentname(String documentname) {
		this.documentname = documentname;
	}

	public String getTxnId() {
		return txnId;
	}

	public void setTxnId(String txnId) {
		this.txnId = txnId;
	}

	public String getTempId() {
		return tempId;
	}

	public void setTempId(String tempId) {
		this.tempId = tempId;
	}

	@Override
	public String toString() {
		return "FileUploadRequest [adddocFile=" + adddocFile + ", documentid=" + documentid + ", documenttxt="
				+ documenttxt + ", documentname=" + documentname + ", txnId=" + txnId + ", tempId=" + tempId + "]";
	}
}
