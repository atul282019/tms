package com.gov.nha.bis.server.requestResponse;

public class InvestigationProcedureMappingRequest {

	   private String procedurecode;
		
		private String investigationcode;
		
		private String investigationstage;
		
		private String statecode;
		
		private Integer schemeid;
		
		private Integer status;

		public String getProcedurecode() {
			return procedurecode;
		}

		public void setProcedurecode(String procedurecode) {
			this.procedurecode = procedurecode;
		}

		public String getInvestigationcode() {
			return investigationcode;
		}

		public void setInvestigationcode(String investigationcode) {
			this.investigationcode = investigationcode;
		}

		public String getInvestigationstage() {
			return investigationstage;
		}

		public void setInvestigationstage(String investigationstage) {
			this.investigationstage = investigationstage;
		}

		public String getStatecode() {
			return statecode;
		}

		public void setStatecode(String statecode) {
			this.statecode = statecode;
		}

		public Integer getSchemeid() {
			return schemeid;
		}

		public void setSchemeid(Integer schemeid) {
			this.schemeid = schemeid;
		}

		public Integer getStatus() {
			return status;
		}

		public void setStatus(Integer status) {
			this.status = status;
		}



}
