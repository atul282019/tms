package com.gov.nha.bis.server.requestResponse;

import java.util.Arrays;

public class VitalStatsProcedureMappingRequest {
	

	private Long[]  vitalid;
	private String procedurecode;
	private String mandatoryStatus;

	
	public Long[]  getVitalid() {
		return vitalid;
	}
	public void setVitalid(Long[]  vitalid) {
		this.vitalid = vitalid;
	}
	public String getProcedurecode() {
		return procedurecode;
	}
	public void setProcedurecode(String procedurecode) {
		this.procedurecode = procedurecode;
	}
	public String getMandatoryStatus() {
		return mandatoryStatus;
	}
	public void setMandatoryStatus(String mandatoryStatus) {
		this.mandatoryStatus = mandatoryStatus;
	}
	@Override
	public String toString() {
		return "VitalStatsProcedureMappingRequest [vitalid=" + Arrays.toString(vitalid) + ", procedurecode="
				+ procedurecode + ", mandatoryStatus=" + mandatoryStatus + "]";
	}
	
	
}
